    public function list(Request $request)
    {

        $page = $request->page ? (int)$request->page : 0;
        $limit = $request->limit ? (int)$request->limit : 20;
        $user_id = $this->user->id;

        $notifications = Notification::where('user_id', $user_id)->orderBy('id','desc')->skip($page*$limit )->take($limit)->get();
        
        $data = [];
        $data['count'] = Notification::count();
        $data['page'] = $page;
        $data['limit'] = $limit;
        $data['items'] = $notifications;
        return $this->responseOK($data);
    }

